import fs from "fs";

export const PatchDLL = async (dllPath: string) => {
    // Create a copy of the file to patch
    if (!fs.existsSync(dllPath + ".bak")) {
        fs.copyFileSync(dllPath, dllPath + ".bak");
    }

    const dll = fs.readFileSync(dllPath);

    for (const patch of PATCHES) {
        const bytes = patch.bytes
            .replace(/\n/g, " ")
            .split(" ")
            .map((byte) => parseInt(byte, 16));

        for (let i = 0; i < bytes.length; i++) {
            const offset = patch.offset + i;
            if (dll[offset] !== bytes[i]) {
                console.log("Patching", offset, bytes[i]);
                dll[offset] = bytes[i];
            }
        }

        if (patch.eof) {
            // Replace remaining bytes with 0x00
            for (let i = patch.offset + bytes.length; i < dll.length; i++) {
                if (dll[i] !== 0x00) {
                    dll[i] = 0x00;
                }
            }
        }
    }

    // Write the patched file
    fs.writeFileSync(dllPath, dll);
};

